package com.gxu.newTbvp.entity;



public class Scene {

    private Integer sceneId;

    private String sceneName;

    private String sceneDesc;

    private String sceneCity;

    private String sceneLevel;

    private String scenePrice;

    private String scenePlace;

    private String sceneImg;

    public Integer getSceneId() {
        return sceneId;
    }

    public void setSceneId(Integer sceneId) {
        this.sceneId = sceneId;
    }

    public String getSceneName() {
        return sceneName;
    }

    public void setSceneName(String sceneName) {
        this.sceneName = sceneName;
    }

    public String getSceneDesc() {
        return sceneDesc;
    }

    public void setSceneDesc(String sceneDesc) {
        this.sceneDesc = sceneDesc;
    }

    public String getSceneCity() {
        return sceneCity;
    }

    public void setSceneCity(String sceneCity) {
        this.sceneCity = sceneCity;
    }

    public String getSceneLevel() {
        return sceneLevel;
    }

    public void setSceneLevel(String sceneLevel) {
        this.sceneLevel = sceneLevel;
    }

    public String getScenePrice() {
        return scenePrice;
    }

    public void setScenePrice(String scenePrice) {
        this.scenePrice = scenePrice;
    }

    public String getScenePlace() {
        return scenePlace;
    }

    public void setScenePlace(String scenePlace) {
        this.scenePlace = scenePlace;
    }

    public String getSceneImg() {
        return sceneImg;
    }

    public void setSceneImg(String sceneImg) {
        this.sceneImg = sceneImg;
    }
}